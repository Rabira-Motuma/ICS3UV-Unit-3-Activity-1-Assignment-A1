// number data type radius
let radius: number = 5.6;

// stores 3.14159 into PI
const PI: number = 3.14159;

// INPUT - none

// PROCESS
// calculate the area
let answer: number = (PI * radius * radius);

// OUTPUT
// display the result
console.log("The area of a circle with a radius of 5.6 cm is " + answer + " cmÂ².");

